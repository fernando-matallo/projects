            <p class="col-wide">Pop art is an art movement that emerged in the United Kingdom and the United States during the mid- to late-1950.</p>

            <img class="imagem1" src="img/popart2.jpg">

            <p class="destaque">Pop art often takes imagery that is currently in use in advertising. Product labeling and logos figure 
            prominently in the imagery chosen by pop artists</p>

            <img class="imagem2" src="img/popart1.jpg">

            <p>The origins of pop art in North America developed differently from Great Britain.[3] In the United States, 
            pop art was a response by artists; it marked a return to hard-edged composition and representational art.
            They used impersonal, mundane reality, irony, and parody to "defuse" the personal symbolism and "painterly 
            looseness" of abstract expressionism</p>

            <p>The Independent Group (IG), founded in London in 1952, is regarded as the precursor to the pop art movement. 
            They were a gathering of young painters, sculptors, architects, writers and critics who were challenging prevailing 
            modernist approaches to culture as well as traditional views of fine art</p>

